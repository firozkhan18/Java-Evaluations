package com.javaprogramto.programs.characters;

public class PrintUpperCaseAlphabets {

	public static void main(String[] args) {

		// first character
		char firstChar = 'A';

		// last character
		char lastChar = 'Z';

		// print uppercase letters from A to Z
		for (char c = firstChar; c <= lastChar; c++) {
			System.out.print(c + " ");

		}

	}

}
