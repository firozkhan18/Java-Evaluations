package com.javaprogramto.programs.characters;

public class PrintLowerCaseAlphabets {

	public static void main(String[] args) {

		// first character
		char firstChar = 'a';

		// last character
		char lastChar = 'z';

		// print lowercase letters from a to z
		for (char c = firstChar; c <= lastChar; c++) {
			System.out.print(c + " ");

		}

	}

}
