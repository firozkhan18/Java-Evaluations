package com.javaprogramto.programs.maths;

public class QuotientRemainderExample {

	public static void main(String[] args) {

		// Creating int variable for number 1
		int number1 = 200;

		// Creating int variable for number 2
		int number2 = 50;

		// Calculating the quotient
		int quotient = number1 / number2;
		
		// Calculating the remainder
		int remainder = number1 % number2;
		
		System.out.println("Quotient value: "+quotient);
		System.out.println("Remainder value with zero: "+remainder);

	}

}
