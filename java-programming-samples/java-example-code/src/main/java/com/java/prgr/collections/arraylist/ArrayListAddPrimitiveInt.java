public class ArrayListAddPrimitiveInt {

	public static void main(String[] args) {

		List l = new ArrayList();
		l.add(10);
		l.add(20);
		l.add(30);
		l.add(40);
		l.add(50);
		l.add(60);

		System.out.println("primitive list values : " + l);

	}

}
